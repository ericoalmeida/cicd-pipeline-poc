const sum = (valueOne, valueTwo) => {
    return valueOne + valueTwo;
}

const sub = (valueOne, valueTwo) => {
    return valueOne - valueTwo;
}

const mult = (valueOne, valueTwo) => {
    return valueOne * valueTwo;
}

const div = (valueOne, valueTwo) => {
    return valueOne / valueTwo;
}

module.exports = {
    sum,
    sub,
    mult,
    div
};